	<h2>Geography</h2>
	<p>Hounslow is separated from Twickenham by Hanworth Road (A314) Nelson Road, Hounslow Road (B361) and Whitton Dene/ Murray Park. Hall Road, Bridge Road, the Hounslow Loop Line, Thornbury Park, Worton Way, the Piccadilly Line, Stucley Road and Osterley Park separate Hounslow and Isleworth. The Norwood Green estate and Industrial area in North Hyde, separate Hounslow and Southall whilst the River Crane and Cranford Park form a natural boundary between Hounslow and Hayes. Historically, Hounslow's traditional western boundary followed the River Crane however it now extends to the Bath Road (A4), Duke of Northumberlands River and Great South-West Road (A30) and back to the river (to include Heathrow Airport).</p>

